// Simple product model
class Cake {
  final int id;
  final String name;
  final String desc;
  final double price;
  final String image;
  final String category;
  Cake({
    required this.id,
    required this.name,
    required this.desc,
    required this.price,
    required this.image,
    required this.category,
  });
}

// Cart item
